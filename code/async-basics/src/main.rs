//! Lesson 01 —— Rust 异步入门（写给第一次接触异步、只在 Go 里用过并发的你）
//!
//! 前置知识：Rust 基础语法、所有权/借用/生命周期；Go 的 goroutine/channel 经验。
//!
//! 本课要建立的三个核心认知（Go 里都没有对应物，需要重建直觉）：
//!   1. async fn 返回的是「惰性的 Future」—— 调用 ≠ 执行！
//!   2. .await = 「等这个 Future 完成；等的期间把线程让出去给别的任务用」
//!   3. 顺序 .await 是串行（耗时相加）；tokio::join! 才是并发（耗时取最大）
//!
//! 运行：cargo run -p async-basics
//! 每行输出都带毫秒时间戳（logln!），盯着时间戳就能"看见"执行顺序。

use std::time::Duration;

use labkit::logln;
use tokio::time::sleep;

/// 一个最简单的异步函数。
///
/// ★ 关键认知：虽然签名写着 `-> String`，但调用 `say(...)` 拿到的**不是** String，
///   而是一个「Future」—— 可以理解成一张**待办卡片**：上面记着要做的事，
///   但还没有人开始做。只有 `.await` 它（或交给 tokio::spawn），才会真正执行。
///
/// 对照 Go：Go 函数被调用就立刻执行，没有"待办卡片"这回事。
async fn say(name: &str, delay_ms: u64) -> String {
    logln!("    say({name}) 开始执行，准备睡 {delay_ms}ms");
    // 异步睡眠：不阻塞线程，只是「挂起自己、让出线程」，运行时可以去跑别的任务；
    // 到点后运行时会回来，从这一行之后继续执行。
    // 对照 Go 的 time.Sleep（Go 运行时其实也是挂起 goroutine，只是对你不可见）。
    sleep(Duration::from_millis(delay_ms)).await;
    logln!("    say({name}) 睡醒了，即将返回");
    format!("你好, {name}")
}

/// 【反面教材】同步阻塞版：用 std::thread::sleep（注意：没有 .await！）。
/// 它会把整个线程干等 delay_ms 毫秒，期间这个线程上的其他异步任务全都动不了。
/// 场景 (5) 会用它演示「一行阻塞代码如何毁掉并发」。
async fn say_blocking(name: &str, delay_ms: u64) -> String {
    logln!("    say_blocking({name}) 开始，用【同步】sleep 卡住线程 {delay_ms}ms");
    std::thread::sleep(Duration::from_millis(delay_ms)); // ❌ 异步代码里千万别这么写
    format!("你好, {name}")
}

#[tokio::main]
async fn main() {
    // =====================================================================
    // (1) Future 是惰性的：调用 async fn ≠ 执行它
    // =====================================================================
    logln!("(1) 惰性演示 ----------------------------------------------------");
    let fut = say("惰性演示1000", 1000); // ← 这一行什么都没执行！只是拿到一张"待办卡片"
    logln!("future 已创建。注意：say 里的第一行日志还没出现 → 函数体确实没执行");

    // .await 才开始执行。对比这行前后的时间戳：卡片这才被领走、开始干活。
    let msg = fut.await;
    logln!("拿到结果: {msg}");
    logln!("（如果只调用 say(...) 而从不 .await，编译器会警告 unused ... Future，");
    logln!("  这是新手第一大坑：调了异步函数却没反应 = 忘了 await）\n");

    // =====================================================================
    // (2) 顺序 .await：一个做完才做下一个（串行）
    // =====================================================================
    logln!("(2) 顺序 await（串行）--------------------------------------------");
    let t = std::time::Instant::now();
    let a = say("A", 100).await; // 等 A 完全结束（100ms）……
    let b = say("B", 100).await; // ……才开始 B（再花 100ms）
    logln!("顺序执行: {a} / {b}");
    logln!("总耗时 {:?} ≈ 200ms：串行，耗时相加\n", t.elapsed());

    // =====================================================================
    // (3) tokio::join!：并发推进多个 Future
    // =====================================================================
    // join! 把几张"待办卡片"捆成一张大卡片一起推进：A 在睡觉时就去推进 B。
    // 对照 Go：go say("A"); go say("B"); wg.Wait()。
    // 但注意区别：join! 不开新线程，是在同一个任务里交替推进
    // （这叫并发 concurrency，不一定是并行 parallelism；
    //   想真正用多核并行要用 tokio::spawn，第 02 课讲）。
    logln!("(3) join! 并发 ----------------------------------------------------");
    let t = std::time::Instant::now();
    let (a, b) = tokio::join!(say("A", 100), say("B", 100));
    logln!("并发执行: {a} / {b}");
    logln!("总耗时 {:?} ≈ 100ms：并发，耗时取最大\n", t.elapsed());

    // =====================================================================
    // (4) 不同耗时的任务并发：总耗时 = 最慢的那个
    // =====================================================================
    logln!("(4) 三个不同耗时的并发 --------------------------------------------");
    let t = std::time::Instant::now();
    let (a, b, c) = tokio::join!(
        say("快100", 100),
        say("中200", 200),
        say("慢300", 300),
    );
    logln!("结果: {a} / {b} / {c}");
    logln!("总耗时 {:?} ≈ 300ms = max(300,100,200)", t.elapsed());
    logln!("★ 回看上面的时间戳：三个 say 的「开始执行」几乎同一毫秒出现，");
    logln!("  而「睡醒了」按 100→200→300 的顺序出现 —— 这就是并发的样子\n");

    // =====================================================================
    // (5) 反面教材：一行同步阻塞，毁掉整个并发
    // =====================================================================
    // say_blocking 用 std::thread::sleep（阻塞线程、不让出）。
    // 即使用 join! "并发"，两个任务也只能一个卡完再卡另一个 → 耗时变回相加！
    logln!("(5) 阻塞的代价 ----------------------------------------------------");
    let t = std::time::Instant::now();
    let (a, b) = tokio::join!(say_blocking("X", 100), say_blocking("Y", 100));
    logln!("阻塞版'并发': {a} / {b}");
    logln!("总耗时 {:?} ≈ 200ms —— 并发失效了！", t.elapsed());
    logln!("★ 铁律：异步代码里绝不要 std::thread::sleep / 同步 IO / 长时间纯计算。");
    logln!("  Go 的调度器会自动补线程救你，Tokio 不会（协作式调度）。");
    logln!("  真有阻塞需求 → spawn_blocking（第 02 课场景 (6)）\n");

    // =====================================================================
    // (6) async 块与 move：不止函数，一段代码块也能变成 Future
    // =====================================================================
    logln!("(6) async 块 ------------------------------------------------------");
    let name = String::from("小块");
    // `async move { ... }` 把一段代码变成 Future；move 表示把外面用到的变量
    // （这里是 name）的**所有权**搬进块里 —— 和闭包的 move 是同一个机制。
    // 为什么常常需要 move？因为 Future 可能比当前作用域活得久，
    // 借用（&name）会过不了借用检查；把所有权搬进去就没有悬垂引用问题。
    // 对照 Go：go func(){ ... }() 里随手捕获外层变量就行（GC 兜底，无所有权概念）。
    let block_fut = async move {
        sleep(Duration::from_millis(1500)).await;
        format!("来自 async 块的问候, {name}")
    };
    logln!("async 块已创建但未执行（它同样是惰性的）");
    let msg = block_fut.await;
    logln!("{msg}");
    logln!("\n第 01 课演示结束。下一课：这些 Future 到底是谁在驱动？→ Tokio 运行时");
}
